class ModoDePreparo():
    
    def __init__(self):
        self.__idModoDePreparo = None
        self.__manualPreparo = None

    def setIdModoDePreparo(self, idmodoDePreparo):
        self.__idModoDePreparo = idmodoDePreparo

    def getIdModoDePreparo(self):
        return self.__idModoDePreparo

    def setManualPreparo(self, manualPreparo):
        self.__idModoDePreparo = manualPreparo

    def getManualPreparo(self):
        return self.__manualPreparo